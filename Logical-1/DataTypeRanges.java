package interview;

public class DataTypeRanges {
public static void main(String[] args) {
	System.out.println("DataType"+"\t"+"Bytes"+"\t"+"Bits"+"\t"+"Min Value"+"\t"+"Max Value");
	System.out.println("-----------------------------------------------------------");
System.out.println(Byte.TYPE+"\t\t"+Byte.BYTES+"\t"+Byte.SIZE
		+"\t"+Byte.MIN_VALUE+"\t\t"+Byte.MAX_VALUE);
System.out.println(Short.TYPE+"\t\t"+Short.BYTES+"\t"+Short.SIZE
		+"\t"+Short.MIN_VALUE+"\t\t"+Short.MAX_VALUE);
System.out.println(Integer.TYPE+"\t\t"+Integer.BYTES+"\t"+Integer.SIZE
		+"\t"+Integer.MIN_VALUE+"\t\t"+Integer.MAX_VALUE);
System.out.println(Long.TYPE+"\t\t"+Long.BYTES+"\t"+Long.SIZE
		+"\t"+Long.MIN_VALUE+"\t\t"+Long.MAX_VALUE);
System.out.println(Float.TYPE+"\t\t"+Float.BYTES+"\t"+Float.SIZE
		+"\t"+Float.MIN_VALUE+"\t\t"+Float.MAX_VALUE);
System.out.println(Double.TYPE+"\t\t"+Double.BYTES+"\t"+Double.SIZE
		+"\t"+Double.MIN_VALUE+"\t\t"+Double.MAX_VALUE);
System.out.println(Character.TYPE+"\t\t"+Character.BYTES+"\t"+Character.SIZE
		+"\t"+Character.MIN_VALUE+"\t\t"+Character.MAX_VALUE);
}
}
